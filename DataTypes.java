public class DataTypes {
  // The 8 primitive data types: 
    // int, byte, short, long, float, double, boolean, char

  int score = 100; // commonly used type for whole numbers

  byte age = 27; // compact data type, great for conserving memory (only 8 bits)

  short year = 2323; // larger than byte but smaller than int

  long population = 781625300L; // for representing vast numbers (64 bits)
  
  float price = 10.99F; // floating-point numbers with single precision (32 bits, or digits in binary form)

  double distance = 384.4; // floating-point numbers with double precision (64 bits, or digits in binary form) 
  // allows for more accuracy but requires more computational resources, memory storage, & data transfer

  boolean isJavaFun = true; // true or false!

  char initial = 'J'; // encapsulates single characters

}
